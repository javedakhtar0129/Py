# item is a variable that takes(iterates) one char of
# string at a time
for item in 'Python':
    print(item)
print()
# [] is used for lists
for item in [1,2,3,4]:
    print(item)
print()
# print({item}) prints elements of list in curly braces
for item in [1,2,3,4]:
    print({item})
print()
# range function prints numbers withing a range
for item in range(0,11):
    print(item)
print()
# range func also have steps (start,end,setps)
for item in range(0,11,2):
    print(item)
print()